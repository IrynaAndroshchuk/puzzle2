package sample;

public class Rectangle {
    int n;
    int width;
    int height;
    int dWidth;
    int dHeight;

    public Rectangle(int n, int width, int height, int dWidth, int dHeight) {
        this.n = n;
        this.width = width;
        this.height = height;
        this.dWidth = dWidth;
        this.dHeight = dHeight;
    }
}
